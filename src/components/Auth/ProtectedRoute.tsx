// components/Auth/ProtectedRoute.tsx - Rota Protegida

import { Navigate } from 'react-router-dom';
import { useAuth } from '../../hooks/useAuth';
import { useInactivityTimeout } from '../../hooks/useInactivityTimeout';

interface ProtectedRouteProps {
  children: React.ReactNode;
  requireCooperativa?: boolean;
  requireEmbarcador?: boolean;
}

export default function ProtectedRoute({
  children,
  requireCooperativa = false,
  requireEmbarcador = false
}: ProtectedRouteProps) {
  const { user, loading, isCooperativa, isEmbarcador } = useAuth();

  // Logout automático após 10 minutos de inatividade (coop e embarcador)
  useInactivityTimeout();

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-50">
        <div className="text-center">
          <svg className="animate-spin h-12 w-12 text-blue-600 mx-auto mb-4" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
            <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
            <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
          </svg>
          <p className="text-gray-600">Carregando...</p>
        </div>
      </div>
    );
  }

  if (!user) {
    return <Navigate to="/login" replace />;
  }

  if (requireCooperativa && !isCooperativa) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-50">
        <div className="text-center">
          <h2 className="text-2xl font-bold text-gray-900 mb-2">Acesso Negado</h2>
          <p className="text-gray-600">Você não tem permissão para acessar esta página.</p>
        </div>
      </div>
    );
  }

  if (requireEmbarcador && !isEmbarcador) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-50">
        <div className="text-center">
          <h2 className="text-2xl font-bold text-gray-900 mb-2">Acesso Negado</h2>
          <p className="text-gray-600">Você não tem permissão para acessar esta página.</p>
        </div>
      </div>
    );
  }

  return <>{children}</>;
}
